package com.example.worksheet6;

public class Stock {
	private String company;
	private double price;
	
	public String getCompany(){
		return company;
	}
	
	public double getPrice(){
		return price;
	}
	
	public void setCompany(String input){
		company = input;
	}
	
	public void setPrice(double d){
		price = d;
	}

}
